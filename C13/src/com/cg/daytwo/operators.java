package com.cg.daytwo;

public class operators {

	public static void main(String[] args) {
		//arithmetic operation(+ - * / %)
		int a=5,b=10;
		float c;
		c=a+b;
		System.out.println("add: "+c);
		c=a-b;
		System.out.println("sub: "+c);
        
		//logical operation
		c=a & b;
		System.out.println("AND: "+c);
		
		
	}

}
